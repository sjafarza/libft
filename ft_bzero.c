#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*v;
	int				i;

	v = (unsigned char *)s;
	i = 0;
	while (i < (int)n)
	{
		v[i] = 0;
		i++;
	}
}
